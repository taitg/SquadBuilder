package com.geordietait.squadbuilder;

/**
 * Class representing the skills for a player
 * @author taitg
 *
 */
public class Skill {
	
	private String type;
	private int rating;
	
	/**
	 * Get the type of skill
	 * @return
	 */
	public String getType() {
		return type;
	}
	
	/**
	 * Get the rating in the particular skill
	 * @return
	 */
	public int getRating() {
		return rating;
	}
}
